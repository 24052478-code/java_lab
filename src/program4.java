class program4{
    public static void main(String[] args) {
        int num = 121;
        int original = num;
        int reverse = 0;//reverse
        while (num > 0) {
            int digit = num % 10;
            reverse = reverse * 10 + digit;
            num = num / 10;
        }//check palindrome
        if (original == reverse) {
            System.out.println(original + " is a Palindrome");
        } else {
            System.out.println(original + " is not a Palindrome");
        }
    }
}
